		<h1>CSS Accessibility Task Force Work Statement</h1>
		<p>The CSS Accessibility Task Force is a task force of the <a href="http://www.w3.org/WAI/APA/">Accessible Platform Architectures Working Group (APA WG)</a>, the <a href="https://www.w3.org/Style/CSS/">Cascading Style Sheets Working Group (CSS WG)</a>, and the <a href="http://www.w3.org/WAI/ARIA/">Accessible Rich Internet Applications Working Group (ARIA WG)</a>. It assists these Working Groups with the work identified below.</p>
		<section>
			<h2>Status</h2>
			<p>This work statement work statement has been approved by the sponsoring Working Groups. The task force is now active.</p>
		</section>
		<section>
			<h2>Objective</h2>
			<p>The mission of the CSS Accessibility Task Force (CSS A11Y TF) is to focus effort on identifying accessibility problems that can be created by use of Cascading Style Sheets (CSS), and to develop solutions. Some accessibility issues are not specific to particular modules but the result of complicated interactions between the overall CSS feature set, underlying host language semantics, user agent features, and assistive technologies. These issues are difficult to address in per-specification reviews normally conducted by the APA WG, and require a base of expertise to develop solutions.</p>
		</section>
		<section>
			<h2>Scope of Work</h2>
			<p>The CSS A11Y TF will:</p>
			<ul>
				<li>Catalog known accessibility problems that can happen when CSS is used, both in individual modules and in larger feature sets or usage patterns;</li>
				<li>Develop techniques and best practices where feasible to guide authors to create accessible CSS usages;</li>
				<li>Create concrete guidance about how user agents should expose accessibility information in CSS-styled content to assistive technologies;</li>
				<li>Determine which CSS accessibility issues need new features to be able to address effectively;</li>
				<li>Work with the CSS, APA, and other Working Groups if needed to provide additional technical features to close accessibility gaps.</li>
			</ul>
		</section>
		<section>
			<h2>Approach</h2>
			<p>The CSS A11Y TF will:</p>
			<ul>
				<li>Develop informal research notes about issues within its scope, most likely in a wiki or GitHub repository but potentially to be published by the APA WG as Working Group Notes;</li>
				<li>Publish, jointy via the APA, ARIA, and CSS WGs, non-normative authoring guidance about CSS accessibility;</li>
				<li>Through the ARIA WG, publish normative user agent implementation guidance about how CSS-styled content should be exposed to accessibility APIs;</li>
				<li>Develop global technical solutions where needed and work with the APA, CSS, and potentially other WGs to get the needed features implemented in relevant standards;</li>
				<li>Work with the CSS WG on individual CSS modules where more localized accessibility features are needed.</li>
			</ul>
		</section>
		<section>
			<h2>Communication</h2>
			<p>CSS Accessibility TF communications and discussions are visible to the public. The <a href=".">task force home page</a> details communication mechanisms in use. The task force will:</p>
			<ul>
				<li>Support synchronous (e.g., teleconferences, face-to-face meetings) and asynchronous (e.g., mailing list discussions, issue trackers, wiki, surveys, etc.) methods of communication;</li>
				<li>Use the procedures of the <a href="https://www.w3.org/WAI/APA/decision-policy">APA Working Group Decision Policy</a> to make decisions;</li>
				<li>Provide updates and feedback to the APA, ARIA, and CSS Working Group teleconferences, as appropriate. These liaisons are primarily the responsibility of the CSS A11Y TF Facilitators;</li>
				<li>Maintain information resulting from its activities (deliverables, participants, face-to-face meetings, teleconferences, etc.) on the <a href=".">CSS A11Y TF home page</a>.</li>
			</ul>
		</section>
		<section>
			<h2 id="participation">Participation</h2>
			<p>Any participant in the APA, ARIA, or CSS WG may participate in the CSS Accessibility TF. Participants should expect to dedicate 3 to 4 hours per week for CSS Accessibility TF work, which is considered part of the overall work commitment to the sponsoring WG: </p>
			<ul>
				<li>Remain current on the CSS Accessibility TF mailing list and respond to postings in a timely manner; </li>
				<li>Provide feedback to editors of specifications via email or by filing bugs; </li>
				<li>Participate in weekly CSS Accessibility TF teleconferences, or send regrets to the CSS Accessibility TF mailing list. </li>
			</ul>
			<p>Participants may also join CSS Accessibility TF sub-groups. Sub-groups take on specific assignments for the CSS Accessibility TF. </p>
			<p>If you are interested in becoming a participant of the CSS Accessibility TF or have any questions regarding its work, contact the task force facilitators. </p>
